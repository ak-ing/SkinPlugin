package com.aking.skin_core.manager;

import android.app.Application;
import android.content.ContentProvider;
import android.content.ContentValues;
import android.content.res.Configuration;
import android.database.Cursor;
import android.net.Uri;
import android.util.Log;

import androidx.annotation.NonNull;
import androidx.annotation.Nullable;

import com.aking.skin_core.widget.SpUtil;

/**
 * Created by Rick at 2023/3/15 21:22.
 */
public class ContextProvider extends ContentProvider {
    @Override
    public boolean onCreate() {
        SpUtil.INSTANCE.init(getContext());
        SkinManager.INSTANCE.init((Application) getContext().getApplicationContext());
        return false;
    }

    @Override
    public void onConfigurationChanged(Configuration newConfig) {
        super.onConfigurationChanged(newConfig);
        Log.i("SkinPlugin", "ContextProvider [onConfigurationChanged]");
        SkinManager.INSTANCE.onConfigChanged(newConfig);
    }

    @Nullable
    @Override
    public Cursor query(@NonNull Uri uri, @Nullable String[] strings, @Nullable String s, @Nullable String[] strings1, @Nullable String s1) {
        return null;
    }

    @Nullable
    @Override
    public String getType(@NonNull Uri uri) {
        return null;
    }

    @Nullable
    @Override
    public Uri insert(@NonNull Uri uri, @Nullable ContentValues contentValues) {
        return null;
    }

    @Override
    public int delete(@NonNull Uri uri, @Nullable String s, @Nullable String[] strings) {
        return 0;
    }

    @Override
    public int update(@NonNull Uri uri, @Nullable ContentValues contentValues, @Nullable String s, @Nullable String[] strings) {
        return 0;
    }
}
